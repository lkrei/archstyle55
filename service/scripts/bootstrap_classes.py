def main() -> int:
    engine = create_engine(DATABASE_URL, pool_pre_ping=True)
    n = 0
    with engine.begin() as conn:
        for name, desc in DESCRIPTIONS.items():
            conn.execute(
                text(
                    """
                    UPDATE classes
                    SET description = :d
                    WHERE name = :n
                    """
                ),
                {"n": name, "d": desc},
            )
            n += 1
    print(f"updated descriptions: {n}")
    return 0


